'use client'
import { signupSchema } from "@/app/schemas/auth";
import { Button } from "@/components/ui/button";
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card";
import { Field, FieldError, FieldGroup, FieldLabel } from "@/components/ui/field";
import { Input } from "@/components/ui/input";
import { authClient } from "@/lib/auth-client";
import { zodResolver } from "@hookform/resolvers/zod";
import { Loader2 } from "lucide-react";
import { useRouter } from "next/navigation";
import { useTransition } from "react";
import { Controller, useForm } from "react-hook-form";
import { toast } from "sonner";
import z from "zod";


export default function SignUpPage() {

    const [isPending, startTransition] = useTransition();

    const router = useRouter();

    const form = useForm({
        resolver: zodResolver(signupSchema),
        defaultValues: {
            name: "",
            email: "",
            password: ""
        },
    });

    function onSubmit(data: z.infer<typeof signupSchema>){

        startTransition( async () =>{
            await authClient.signUp.email({
                email: data.email,
                name: data.name,
                password: data.password,
                fetchOptions: {
                    onSuccess: () => {
                        toast.success("Account created successfully")
                        router.push("/")
                    }, 
                    onError: (error) => {
                        toast.error(error.error.message)
                    }
                }
            })
        })
    }
   
        

  return (
    <Card>
        <CardHeader>
            <CardTitle>
                Sign up
            </CardTitle>
            <CardDescription>Get started</CardDescription>
        </CardHeader>

        <CardContent>
            <form onSubmit={form.handleSubmit(onSubmit)}>
                <FieldGroup className="gap-y-6">
                    <Controller name="name" control={form.control} render={({ field, fieldState }) => (
                        <Field>
                            <FieldLabel>Full Name</FieldLabel>
                            <Input aria-invalid={fieldState.invalid} placeholder="enter your name"  {...field} />
                            {fieldState.invalid && (
                                <FieldError errors={[fieldState.error]}/>
                            )}

                        </Field>
                    )} />

                    <Controller name="email" control={form.control} render={({ field, fieldState }) => (
                        <Field>
                            <FieldLabel>Email</FieldLabel>
                            <Input aria-invalid={fieldState.invalid} placeholder="abc@gmail.com" type="email" {...field} />
                            {fieldState.invalid && (
                                <FieldError errors={[fieldState.error]}/>
                            )}

                        </Field>
                    )} />

                    <Controller name="password" control={form.control} render={({ field, fieldState }) => (
                        <Field>
                            <FieldLabel>Password</FieldLabel>
                            <Input aria-invalid={fieldState.invalid} placeholder="123456" type="password" {...field} />
                            {fieldState.invalid && (
                                <FieldError errors={[fieldState.error]}/>
                            )}

                        </Field>
                    )} />

                    <Button disabled={isPending} className="mt-2">{isPending  ? (
                        <>
                        <Loader2 className="size-4 animate-spin" />
                        <span>Loading...</span>
                        </>
                         ) : (
                            <span>Signup</span>
                        )}</Button>
                </FieldGroup>
            </form>
        </CardContent>
    </Card>
  );
}
